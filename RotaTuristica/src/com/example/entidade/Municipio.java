package com.example.entidade;

public class Municipio {
	
	 
	    private Long id;
	    private String nome;
	    private Long poloId;
	    
		public Long getId() {
			return id;
		}
		public void setId(Long id) {
			this.id = id;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public Long getPoloId() {
			return poloId;
		}
		public void setPoloId(Long poloId) {
			this.poloId = poloId;
		}
		
}
